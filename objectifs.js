// Élément modal
const modal = document.querySelector('.objectif-modal');
const modalTitle = document.querySelector('.objectif-title');
const modalContent = document.querySelector('.objectif-content');
const closeBtn = document.querySelector('.objectif-close');
const modalBackdrop = document.querySelector('.objectif-modal-backdrop');

// Ouvrir le modal
document.querySelectorAll('.objectif-card').forEach(card => {
    card.addEventListener('click', function() {
        const objectifKey = this.dataset.objectif;
        const objectif = objectifs[objectifKey];
        
        if (objectif) {
            modalTitle.textContent = objectif.title;
            modalContent.innerHTML = objectif.content;
            modal.classList.add('open');
            modal.setAttribute('aria-hidden', 'false');
            closeBtn.focus();
        }
    });

    // Support clavier (Enter et Space)
    card.addEventListener('keydown', function(e) {
        if (e.key === 'Enter' || e.key === ' ') {
            e.preventDefault();
            this.click();
        }
    });
});

// Fermer le modal au clic du bouton X
closeBtn.addEventListener('click', closeModal);

// Fermer le modal au clic sur le backdrop
modalBackdrop.addEventListener('click', closeModal);

// Fermer le modal à la touche Échap
document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape' && modal.classList.contains('open')) {
        closeModal();
    }
});

function closeModal() {
    modal.classList.remove('open');
    modal.setAttribute('aria-hidden', 'true');
}
